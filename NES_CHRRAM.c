void drawChars()
{
  byte x, y, z = 0;
  for (y = 0; y < 16; ++y)
  {
    for (x = 0; x < 16; ++x)
    {
      vram_adr(NTADR_A(x,y));
      vram_put(z);
      ++z;
    }
  }
}

void UploadCharset()
{
  int x = 0;
  unsigned char *chrdata = (unsigned char*)0xA000;  
  vram_adr(0);
  for (x = 0; x < 0x2000; ++x)
  {
    vram_put(chrdata[x]);
  }
}

